func TestAutoGeneratedKeyFile_PermissionCheckOnRead(t *testing.T) {
	if runtime.GOOS == "windows" {
		t.Skip("Unix file permissions not applicable on Windows")
	}

	tmpDir := t.TempDir()
	tmpKeyFile := filepath.Join(tmpDir, ".encryption_key")

	t.Setenv("ENCRYPTION_KEY", "")
	oldKeyFilePath := keyFilePath
	keyFilePath = tmpKeyFile
	t.Cleanup(func() {
		ResetEncryptionKey()
		keyFilePath = oldKeyFilePath
	})

	// Generate a valid key file first.
	ResetEncryptionKey()
	token := "permission-test-token"
	encrypted, err := EncryptToken(token)
	if err != nil {
		t.Fatalf("initial encrypt: %v", err)
	}

	// Widen permissions to simulate insecure file (group/world readable).
	if err := os.Chmod(tmpKeyFile, 0644); err != nil {
		t.Fatalf("chmod: %v", err)
	}
	ResetEncryptionKey()

	// Reading the key should still succeed (permissions get tightened automatically).
	decrypted, err := DecryptToken(encrypted)
	if err != nil {
		t.Fatalf("decrypt with loose permissions: %v", err)
	}
	if decrypted != token {
		t.Errorf("expected %q, got %q", token, decrypted)
	}

	// Verify permissions were tightened back to 0600.
	info, err := os.Stat(tmpKeyFile)
	if err != nil {
		t.Fatalf("stat key file: %v", err)
	}
	perm := info.Mode().Perm()
	if perm != 0600 {
		t.Errorf("expected permissions 0600, got %04o", perm)
	}
}

