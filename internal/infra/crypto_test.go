func TestAutoGeneratedKeyFile_TrailingNewlineHandled(t *testing.T) {
	tmpDir := t.TempDir()
	tmpKeyFile := filepath.Join(tmpDir, ".encryption_key")

	t.Setenv("ENCRYPTION_KEY", "")
	oldKeyFilePath := keyFilePath
	keyFilePath = tmpKeyFile
	t.Cleanup(func() {
		ResetEncryptionKey()
		keyFilePath = oldKeyFilePath
	})

	// First, generate a valid key file.
	ResetEncryptionKey()
	token := "test-token"
	encrypted, err := EncryptToken(token)
	if err != nil {
		t.Fatalf("initial encrypt: %v", err)
	}

	// Append a trailing newline (as editors often do).
	data, err := os.ReadFile(tmpKeyFile)
	if err != nil {
		t.Fatalf("read key file: %v", err)
	}
	if err := os.WriteFile(tmpKeyFile, append(data, '\n'), 0600); err != nil {
		t.Fatalf("write key file with newline: %v", err)
	}
	ResetEncryptionKey()

	// Should still decrypt correctly — trailing \n is trimmed.
	decrypted, err := DecryptToken(encrypted)
	if err != nil {
		t.Fatalf("decrypt after newline appended: %v", err)
	}
	if decrypted != token {
		t.Errorf("expected %q, got %q", token, decrypted)
	}
}
