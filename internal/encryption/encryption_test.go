func TestAutoGeneratedKeyFile_CreatesAndReuses(t *testing.T) {
	tmpDir := t.TempDir()
	tmpKeyFile := filepath.Join(tmpDir, ".encryption_key")

	t.Setenv("ENCRYPTION_KEY", "")

	oldKeyFilePath := keyFilePath
	keyFilePath = tmpKeyFile
	t.Cleanup(func() {
		ResetEncryptionKey()
		keyFilePath = oldKeyFilePath
	})
	ResetEncryptionKey()

	token := "my-secret-token"
	encrypted, err := Encrypt(token)
	if err != nil {
		t.Fatalf("first encrypt: %v", err)
	}

	data, err := os.ReadFile(tmpKeyFile)
	if err != nil {
		t.Fatalf("read key file: %v", err)
	}
	if len(data) != 64 {
		t.Fatalf("expected 64 hex chars, got %d", len(data))
	}
	if _, err := hex.DecodeString(string(data)); err != nil {
		t.Fatalf("key file is not valid hex: %v", err)
	}

	ResetEncryptionKey()

	decrypted, err := Decrypt(encrypted)
	if err != nil {
		t.Fatalf("decrypt after reset: %v", err)
	}
	if decrypted != token {
		t.Errorf("expected %q, got %q", token, decrypted)
	}
}

