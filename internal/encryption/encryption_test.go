func TestDecryptField_CorruptedCiphertext(t *testing.T) {
	resetCryptoForTest(t)

	// Encrypt a value, then corrupt the ciphertext. Since it's still valid base64
	// with enough bytes, DecryptField should return an error (not silently fall back).
	encrypted, err := EncryptField("secret")
	if err != nil {
		t.Fatalf("encrypt: %v", err)
	}

	// Corrupt by flipping a character in the middle of the base64 string.
	corrupted := []byte(encrypted)
	mid := len(corrupted) / 2
	if corrupted[mid] == 'A' {
		corrupted[mid] = 'B'
	} else {
		corrupted[mid] = 'A'
	}

	_, err = DecryptField(string(corrupted))
	if err == nil {
		t.Error("expected error for corrupted ciphertext, got nil")
	}
}

